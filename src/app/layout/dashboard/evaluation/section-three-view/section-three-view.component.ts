  updateMultiOptionData(value, data) {
    this.loading = true;
    let selectedOption;
    for (const option of data.optionList) {
      if (value == option.id || value == option.title) {
        selectedOption = option;
        break;
      }
    }
    if (value) {
      if (this.isEditable) {
        const obj = {
          surveyId: data.surveyId,
          msdId: data.id,
          fieldId: data.fieldId,
          formId: data.formId,
          title: data.question,
          categoryTitle: this.data.sectionTitle,
          newValueId: selectedOption?.id || -1,
          newValue: selectedOption?.title || value,
          evaluatorId: this.evaluatorId,
          newChild: data.newChild || "N",
        };

        this.httpService.updateSurveyData(obj).subscribe((res: any) => {
          if (res.success) {
            this.loading = false;
            this.toastr.success("Data Updated Successfully");
          } else {
            this.toastr.error(res.message, "Update Data");
          }
        });
      } else {
        this.toastr.error("Operation not allowed. Please login with the relevant Id", "Error");
      }
    } else {
      this.toastr.error("Value is Incorrect");
      this.loading = false;
    }

    // ✅ Handle parent conditions for 304 and 286
    if (this.projectType === "PMI_CENSUS") {
      if (data.fieldId === 304 && value === "YES") {
        this.addChildQuestions(304);
      }
      if (data.fieldId === 286 && value === "YES") {
        this.addChildQuestions(286);
      }
    }
  }

  updateTextData(value) {
    this.loading = true;
    if (value.answer) {
      if (this.isEditable) {
        const obj = {
          surveyId: value.surveyId || this.data?.surveyId,
          formId: value.formId || this.data?.formId,
          msdId: value.id,
          fieldId: value.fieldId,
          newValue: value.answer,
          newValueId: -1,
          title: value.question,
          categoryTitle: this.data.sectionTitle,
          evaluatorId: this.evaluatorId,
          newChild: value.newChild || "N"
        };

        this.httpService.updateSurveyData(obj).subscribe((res: any) => {
          if (res.success) {
            this.loading = false;
            this.toastr.success("Data Updated Successfully");
          } else {
            this.toastr.error(res.message, "Update Data");
          }
        });
      } else {
        this.toastr.error("Operation not allowed. Please login with the relevant Id", "Error");
      }
    } else {
      this.toastr.error("Value is Incorrect");
      this.loading = false;
    }
  }

  // ✅ Inject child questions based on parent field
  addChildQuestions(parentId: number) {
    if (this.projectType !== "PMI_CENSUS") return;

    const parent = this.formData.find(f => f.value.fieldId === parentId);
    const parentSurveyId = parent?.value?.surveyId || parent?.surveyId || this.data?.surveyId;
    const parentFormId   = parent?.value?.formId   || parent?.formId   || this.data?.formId;

    const children = parentId === 304 ? this.childQuestions : this.childQuestions286;

    children.forEach((child) => {
      if (!this.formData.find((f) => f.value.fieldId === child.fieldId)) {
        this.formData.push({
          value: { ...child, newChild: "Y", surveyId: parentSurveyId, formId: parentFormId }
        });
      }
    });
  }
